drop table if exists public.kdn03_scans cascade;
drop table if exists public.kdn03_fixes cascade;
drop table if exists public.kdn03_resources cascade;
drop table if exists public.kdn03_threats cascade;
drop table if exists public.kdn03_notices cascade;
drop table if exists public.kdn03_assets cascade;

create table public.kdn03_assets (
  id uuid primary key default gen_random_uuid(),
  hostname text not null, ip text default '', os_type text default '', os_version text default '',
  department text default '', "owner" text default '', location text default '', note text default '',
  created_at timestamptz not null default now()
);
create table public.kdn03_scans (
  id uuid primary key default gen_random_uuid(),
  asset_id uuid references public.kdn03_assets(id) on delete set null,
  hostname text not null, scan_date timestamptz not null, file_name text default '',
  uploaded_at timestamptz not null default now(), uploaded_by text default '',
  total int not null default 0, vuln_count int not null default 0, good_count int not null default 0,
  manual_count int not null default 0, score numeric(5,1) not null default 0,
  results jsonb not null default '[]'::jsonb
);
create table public.kdn03_fixes (
  id uuid primary key default gen_random_uuid(),
  asset_id uuid references public.kdn03_assets(id) on delete set null,
  hostname text not null, fix_date timestamptz not null, file_name text default '',
  uploaded_at timestamptz not null default now(), items_arg text default '',
  total int not null default 0, fixed_count int not null default 0, reported_count int not null default 0,
  manual_count int not null default 0, fail_count int not null default 0,
  items jsonb not null default '[]'::jsonb
);
create table public.kdn03_notices (
  id uuid primary key default gen_random_uuid(),
  category text not null default '일반', title text not null, body text default '',
  author text default '관리자', pinned boolean not null default false, views int not null default 0,
  created_at timestamptz not null default now(), updated_at timestamptz not null default now()
);
create table public.kdn03_threats (
  id uuid primary key default gen_random_uuid(),
  title text not null, cve text default '', severity text not null default '중간',
  source text default '', source_url text default '', published_date text default '',
  tags jsonb not null default '[]'::jsonb, related_items jsonb not null default '[]'::jsonb,
  body text default '', author text default '관리자', views int not null default 0,
  created_at timestamptz not null default now(), updated_at timestamptz not null default now()
);
create table public.kdn03_resources (
  id uuid primary key default gen_random_uuid(),
  category text not null default '가이드', title text not null, description text default '',
  file_name text default '', file_path text default '', file_url text default '',
  file_size bigint default 0, mime text default '', author text default '관리자',
  downloads int not null default 0,
  created_at timestamptz not null default now(), updated_at timestamptz not null default now()
);
alter table public.kdn03_assets    enable row level security;
alter table public.kdn03_scans     enable row level security;
alter table public.kdn03_fixes     enable row level security;
alter table public.kdn03_notices   enable row level security;
alter table public.kdn03_threats   enable row level security;
alter table public.kdn03_resources enable row level security;
create policy "kdn03 assets all"    on public.kdn03_assets    for all to authenticated using (true) with check (true);
create policy "kdn03 scans all"     on public.kdn03_scans     for all to authenticated using (true) with check (true);
create policy "kdn03 fixes all"     on public.kdn03_fixes     for all to authenticated using (true) with check (true);
create policy "kdn03 notices all"   on public.kdn03_notices   for all to authenticated using (true) with check (true);
create policy "kdn03 threats all"   on public.kdn03_threats   for all to authenticated using (true) with check (true);
