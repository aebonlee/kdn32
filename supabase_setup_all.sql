drop trigger if exists kdn29_on_auth_user_created on auth.users;
drop table if exists public.kdn29_comments cascade;
drop table if exists public.kdn29_posts cascade;
drop table if exists public.kdn29_profiles cascade;
create table public.kdn29_profiles (
  id uuid primary key references auth.users(id) on delete cascade, email text not null
);
alter table public.kdn29_profiles enable row level security;
create policy "kdn29 prof read" on public.kdn29_profiles for select using (true);
create policy "kdn29 prof ins"  on public.kdn29_profiles for insert with check (auth.uid()=id);
create policy "kdn29 prof upd"  on public.kdn29_profiles for update using (auth.uid()=id);
create or replace function public.kdn29_handle_new_user()
returns trigger language plpgsql security definer as $$
begin
  insert into public.kdn29_profiles (id,email) values (new.id,new.email) on conflict (id) do nothing;
  return new;
end; $$;
create trigger kdn29_on_auth_user_created after insert on auth.users
  for each row execute procedure public.kdn29_handle_new_user();
create table public.kdn29_posts (
  id uuid primary key default gen_random_uuid(),
  title text not null, content text not null,
  author_id uuid not null references public.kdn29_profiles(id) on delete cascade,
  is_notice boolean not null default false, created_at timestamptz not null default now()
);
alter table public.kdn29_posts enable row level security;
create policy "kdn29 posts read" on public.kdn29_posts for select using (true);
create policy "kdn29 posts ins"  on public.kdn29_posts for insert with check (auth.uid()=author_id);
create policy "kdn29 posts upd"  on public.kdn29_posts for update using (auth.uid()=author_id);
create policy "kdn29 posts del"  on public.kdn29_posts for delete using (auth.uid()=author_id);
create table public.kdn29_comments (
  id uuid primary key default gen_random_uuid(),
  post_id uuid not null references public.kdn29_posts(id) on delete cascade,
  content text not null,
  author_id uuid not null references public.kdn29_profiles(id) on delete cascade,
  created_at timestamptz not null default now()
);
alter table public.kdn29_comments enable row level security;
create policy "kdn29 cmt read" on public.kdn29_comments for select using (true);
create policy "kdn29 cmt ins"  on public.kdn29_comments for insert with check (auth.uid()=author_id);
create policy "kdn29 cmt del"  on public.kdn29_comments for delete using (auth.uid()=author_id);

