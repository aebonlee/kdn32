-- ============================================================================
-- kdn01  운세앱 게시판 (posts, board_comments)
-- ============================================================================
drop table if exists public.kdn01_board_comments cascade;
drop table if exists public.kdn01_posts cascade;

create table public.kdn01_posts (
  id uuid primary key default gen_random_uuid(),
  board text not null, title text not null, content text not null,
  author text not null, email text default '',
  created_at timestamptz default now(), view_count int default 0, comment_count int default 0
);
create table public.kdn01_board_comments (
  id uuid primary key default gen_random_uuid(),
  post_id uuid not null references public.kdn01_posts(id) on delete cascade,
  author text not null, content text not null, created_at timestamptz default now()
);
create index if not exists idx_kdn01_posts_board   on public.kdn01_posts(board);
create index if not exists idx_kdn01_posts_created on public.kdn01_posts(created_at desc);
create index if not exists idx_kdn01_comments_post on public.kdn01_board_comments(post_id);
alter table public.kdn01_posts enable row level security;
alter table public.kdn01_board_comments enable row level security;
create policy "kdn01 read posts"    on public.kdn01_posts for select using (true);
create policy "kdn01 insert posts"  on public.kdn01_posts for insert with check (true);
create policy "kdn01 update posts"  on public.kdn01_posts for update using (true);
create policy "kdn01 read comments"   on public.kdn01_board_comments for select using (true);
create policy "kdn01 insert comments" on public.kdn01_board_comments for insert with check (true);

